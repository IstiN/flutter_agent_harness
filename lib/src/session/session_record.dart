/// The JSONL session record model: the append-only tree of entries that
/// makes up a session, plus the file header.
///
/// Ported from pi-mono `packages/agent/src/harness/types.ts`
/// (`SessionTreeEntry` union) and `harness/session/jsonl-storage.ts`
/// (`SessionHeader`). A session file is a header line
/// (`{"type":"session","version":3,...}`) followed by one JSON record per
/// line; records form a tree through [SessionRecord.parentId], and `leaf`
/// records mark navigation of the active branch.
library;

import '../context.dart';
import '../types.dart';

/// The session header — always the first line of a JSONL session file.
///
/// Ported from pi's `SessionHeader` (version 3).
final class SessionHeader {
  /// Creates a [SessionHeader].
  const SessionHeader({
    required this.id,
    required this.timestamp,
    required this.cwd,
    this.parentSessionPath,
    this.metadata,
  });

  /// On-disk format version. Only version 3 is supported.
  static const version = 3;

  /// Unique session id.
  final String id;

  /// When the session was created.
  final DateTime timestamp;

  /// Working directory the session belongs to.
  final String cwd;

  /// Path of the session this one was forked from, if any.
  final String? parentSessionPath;

  /// Free-form application metadata.
  final Map<String, dynamic>? metadata;

  /// Serializes to the header JSON line (without the trailing newline).
  Map<String, dynamic> toJson() => {
    'type': 'session',
    'version': version,
    'id': id,
    'timestamp': timestamp.toIso8601String(),
    'cwd': cwd,
    if (parentSessionPath != null) 'parentSession': parentSessionPath,
    if (metadata != null) 'metadata': metadata,
  };

  /// Deserializes and validates a header JSON map.
  ///
  /// Throws [FormatException] on any invalid field, mirroring pi's
  /// `parseHeaderLine` validation.
  factory SessionHeader.fromJson(Map<String, dynamic> json) {
    FormatException invalid(String message) =>
        FormatException('Invalid session header: $message');
    if (json['type'] != 'session') {
      throw invalid('first line is not a valid session header');
    }
    if (json['version'] != version) {
      throw invalid('unsupported session version');
    }
    if (json['id'] is! String || (json['id'] as String).isEmpty) {
      throw invalid('session header is missing id');
    }
    if (json['timestamp'] is! String || (json['timestamp'] as String).isEmpty) {
      throw invalid('session header is missing timestamp');
    }
    if (json['cwd'] is! String || (json['cwd'] as String).isEmpty) {
      throw invalid('session header is missing cwd');
    }
    final parentSession = json['parentSession'];
    if (parentSession != null && parentSession is! String) {
      throw invalid('session header parentSession must be a string');
    }
    final metadata = json['metadata'];
    if (metadata != null && metadata is! Map<String, dynamic>) {
      throw invalid('session header metadata must be an object');
    }
    return SessionHeader(
      id: json['id'] as String,
      timestamp: DateTime.parse(json['timestamp'] as String),
      cwd: json['cwd'] as String,
      parentSessionPath: parentSession as String?,
      metadata: metadata as Map<String, dynamic>?,
    );
  }
}

/// One entry in the append-only session tree.
///
/// Sealed counterpart of pi's `SessionTreeEntry` discriminated union. Every
/// record has a unique [id], an optional [parentId] forming the tree, and a
/// [timestamp]. Dispatch on the concrete type (or [type]) to read the
/// payload.
sealed class SessionRecord {
  /// Creates a [SessionRecord].
  const SessionRecord({
    required this.id,
    required this.parentId,
    required this.timestamp,
  });

  /// The type discriminator written to JSON (pi's `type` field).
  String get type;

  /// Unique record id (short random id generated by the storage).
  final String id;

  /// Id of the parent record, or `null` for a tree root.
  final String? parentId;

  /// When the record was created.
  final DateTime timestamp;

  /// Serializes to a JSON map with the `type` discriminator (pi shape).
  Map<String, dynamic> toJson() => {
    'type': type,
    'id': id,
    'parentId': parentId,
    'timestamp': timestamp.toIso8601String(),
    ...payloadJson(),
  };

  /// Type-specific fields merged into [toJson] by subclasses.
  Map<String, dynamic> payloadJson();

  /// Deserializes a record JSON map, dispatching on `type`.
  ///
  /// Throws [FormatException] for unknown types or invalid base fields,
  /// mirroring pi's `parseEntryLine` validation.
  factory SessionRecord.fromJson(Map<String, dynamic> json) {
    FormatException invalid(String message) =>
        FormatException('Invalid session record: $message');
    final type = json['type'];
    if (type is! String) throw invalid('missing entry type');
    final id = json['id'];
    if (id is! String || id.isEmpty) throw invalid('missing entry id');
    final parentId = json['parentId'];
    if (parentId != null && parentId is! String) {
      throw invalid('invalid parentId');
    }
    final timestampRaw = json['timestamp'];
    if (timestampRaw is! String || timestampRaw.isEmpty) {
      throw invalid('missing timestamp');
    }
    final timestamp = DateTime.parse(timestampRaw);
    return switch (type) {
      'message' => MessageRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        message: messageFromJson(
          (json['message'] as Map?)?.cast<String, dynamic>() ??
              const <String, dynamic>{},
        ),
      ),
      'thinking_level_change' => ThinkingLevelChangeRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        thinkingLevel: json['thinkingLevel'] as String? ?? 'off',
      ),
      'model_change' => ModelChangeRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        provider: json['provider'] as String? ?? '',
        modelId: json['modelId'] as String? ?? '',
      ),
      'active_tools_change' => ActiveToolsChangeRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        activeToolNames: [
          for (final name in (json['activeToolNames'] as List?) ?? const [])
            name as String,
        ],
      ),
      'compaction' => CompactionRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        summary: json['summary'] as String? ?? '',
        firstKeptEntryId: json['firstKeptEntryId'] as String? ?? '',
        tokensBefore: json['tokensBefore'] as int? ?? 0,
        details: json['details'],
        fromHook: json['fromHook'] as bool?,
      ),
      'branch_summary' => BranchSummaryRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        fromId: json['fromId'] as String? ?? '',
        summary: json['summary'] as String? ?? '',
        details: json['details'],
        fromHook: json['fromHook'] as bool?,
      ),
      'custom' => CustomRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        customType: json['customType'] as String? ?? '',
        data: json['data'],
      ),
      'checkpoint' => CheckpointRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        messageCount: json['messageCount'] as int? ?? 0,
        goal: json['goal'] as String?,
      ),
      'custom_message' => CustomMessageRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        customType: json['customType'] as String? ?? '',
        content: _parseCustomMessageContent(json['content']),
        display: json['display'] as bool? ?? false,
        details: json['details'],
      ),
      'label' => LabelRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        targetId: json['targetId'] as String? ?? '',
        label: json['label'] as String?,
      ),
      'session_info' => SessionInfoRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        name: json['name'] as String?,
      ),
      'leaf' => LeafRecord(
        id: id,
        parentId: parentId as String?,
        timestamp: timestamp,
        targetId: json['targetId'] as String?,
      ),
      _ => throw invalid('unknown entry type: $type'),
    };
  }
}

Object _parseCustomMessageContent(Object? content) {
  if (content is String) return content;
  return [
    for (final block in (content as List?) ?? const [])
      ContentBlock.fromJson((block as Map).cast<String, dynamic>()),
  ];
}

/// A conversation message appended to the tree.
///
/// Ported from pi's `MessageEntry`.
final class MessageRecord extends SessionRecord {
  /// Creates a [MessageRecord].
  const MessageRecord({
    required super.id,
    required super.parentId,
    required super.timestamp,
    required this.message,
  });

  /// The conversation message (user, assistant, or tool result).
  final Message message;

  @override
  String get type => 'message';

  @override
  Map<String, dynamic> payloadJson() => {'message': message.toJson()};
}

