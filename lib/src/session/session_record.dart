  /// Creates a [LabelRecord].
  const LabelRecord({
    required super.id,
    required super.parentId,
    required super.timestamp,
    required this.targetId,
    this.label,
  });

  /// Id of the record being labeled.
  final String targetId;

  /// The label text; `null` (or empty/whitespace) removes the label.
  final String? label;

  @override
  String get type => 'label';

  @override
  Map<String, dynamic> payloadJson() => {
    'targetId': targetId,
    if (label != null) 'label': label,
  };
}

