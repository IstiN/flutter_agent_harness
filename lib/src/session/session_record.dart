/// Marks a compaction point: [summary] replaces everything before
/// [firstKeptEntryId] when the context is rebuilt.
///
/// Ported from pi's `CompactionEntry`. Written by the compaction pipeline
/// (a later phase); the record type is defined here so the tree and context
/// projection already understand it.
final class CompactionRecord extends SessionRecord {
  /// Creates a [CompactionRecord].
  const CompactionRecord({
    required super.id,
    required super.parentId,
    required super.timestamp,
    required this.summary,
    required this.firstKeptEntryId,
    required this.tokensBefore,
    this.details,
    this.fromHook,
  });

  /// LLM-generated summary of the compacted history.
  final String summary;

  /// Id of the first record kept verbatim after the summary.
  final String firstKeptEntryId;

  /// Token count of the context before compaction.
  final int tokensBefore;

  /// Optional structured details (e.g. file operations performed).
  final Object? details;

  /// Whether the compaction was triggered by a hook rather than the loop.
  final bool? fromHook;

  @override
  String get type => 'compaction';

  @override
  Map<String, dynamic> payloadJson() => {
    'summary': summary,
    'firstKeptEntryId': firstKeptEntryId,
    'tokensBefore': tokensBefore,
    if (details != null) 'details': details,
    if (fromHook != null) 'fromHook': fromHook,
  };
}

/// Marks a branch the conversation navigated away from, with an optional
