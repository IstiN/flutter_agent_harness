/// An application-defined record that projects into model context as a user
/// message.
///
/// Ported from pi's `CustomMessageEntry`.
final class CustomMessageRecord extends SessionRecord {
  /// Creates a [CustomMessageRecord].
  const CustomMessageRecord({
    required super.id,
    required super.parentId,
    required super.timestamp,
    required this.customType,
    required this.content,
    required this.display,
    this.details,
  });

  /// Application-defined discriminator.
  final String customType;

  /// Message content: a [String] or a `List<ContentBlock>` of
  /// [TextContent]/[ImageContent].
  final Object content;

  /// Whether the message is shown in the UI (does not affect context).
  final bool display;

  /// Optional structured details.
  final Object? details;

  @override
  String get type => 'custom_message';

  @override
  Map<String, dynamic> payloadJson() => {
    'customType': customType,
    'content': content is String
        ? content
        : [for (final block in content as List<ContentBlock>) block.toJson()],
    'display': display,
    if (details != null) 'details': details,
  };
}

/// Attaches (or removes, when [label] is null) a human-readable label to
/// another record, e.g. a bookmark in the tree UI.
///
/// Ported from pi's `LabelEntry`.
