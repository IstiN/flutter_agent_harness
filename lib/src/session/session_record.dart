/// the tree root).
final class LeafRecord extends SessionRecord {
  /// Creates a [LeafRecord].
  const LeafRecord({
    required super.id,
    required super.parentId,
    required super.timestamp,
    this.targetId,
  });

  /// The new active leaf, or `null` for the tree root.
  final String? targetId;

  @override
  String get type => 'leaf';

  @override
  Map<String, dynamic> payloadJson() => {'targetId': targetId};
}
