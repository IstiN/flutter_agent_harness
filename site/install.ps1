# в”Җв”Җ 1. Detect platform в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
$arch = if ([Environment]::Is64BitOperatingSystem) { "x64" } else { "ia32" }
# ARM64 Windows detection via processor architecture.
if ($env:PROCESSOR_ARCHITECTURE -eq "ARM64" -or $env:PROCESSOR_ARCHITEW6432 -eq "ARM64") {
    $arch = "arm64"
}
$assetName = "fa-windows-$arch.exe"

# в”Җв”Җ 2. Resolve install directory в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
# DMTools-style: keep every binary in a dedicated bin directory and prepend
# that directory to the user PATH. This guarantees the command is available
# immediately after installation in the current PowerShell session.
$installDir = Join-Path $env:LOCALAPPDATA "Fa"
$binDir = Join-Path $installDir "bin"

if (-not (Test-Path $binDir)) {
    New-Item -ItemType Directory -Force -Path $binDir | Out-Null
}
$target = Join-Path $binDir "$BinaryName.exe"

# в”Җв”Җ 3. Resolve download URL в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
$downloadUrl = "https://github.com/$Repo/releases/latest/download/$assetName"

# в”Җв”Җ 4. Install binary (or fall back to Dart) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
Write-Info "Downloading Fa for windows-$arch..."
try {
    $progressPreference = 'silentlyContinue'
    Invoke-WebRequest -Uri $downloadUrl -OutFile $target -UseBasicParsing -MaximumRedirection 5
    $progressPreference = 'Continue'
    Write-Ok "Downloaded $target"
} catch {
    # A 404 usually means no binary was published for this platform yet.
    $status = $_.Exception.Response.StatusCode.value__
    if ($status -eq 404) {
        Write-Warn "No prebuilt binary found for windows-$arch. Falling back to Dart pub global activate."
    } else {
        Write-Warn "Binary download failed (HTTP $status). Falling back to Dart pub global activate."
    }
    $dart = Get-Command dart -ErrorAction SilentlyContinue
    if (-not $dart) {
        Write-Err "the Dart SDK ('dart') is not on your PATH, and no prebuilt binary is available for windows-$arch."
        Write-Host ""
        Write-Host "  Install Flutter (includes Dart) from:"
        Write-Host ""
        Write-Host "    https://docs.flutter.dev/get-started/install"
        Write-Host ""
        exit 1
    }
    Write-Info "Activating flutter_agent_harness from pub.dev..."
    & dart pub global activate flutter_agent_harness
    $pubCacheBin = if ($env:PUB_CACHE_BIN) { $env:PUB_CACHE_BIN } else { Join-Path $env:LOCALAPPDATA "Pub\Cache\bin" }
    if (-not (Test-Path $pubCacheBin)) { $pubCacheBin = Join-Path $env:APPDATA "Pub\Cache\bin" }
    $binDir = $pubCacheBin
    $target = Join-Path $binDir "$BinaryName.bat"
}

# в”Җв”Җ 5. Ensure the bin directory is on the user PATH в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
$userPath = [Environment]::GetEnvironmentVariable("Path", "User")
$pathParts = $userPath -split ';' | ForEach-Object { $_.Trim() } | Where-Object { $_ }
if ($binDir -notin $pathParts) {
    Write-Info "Adding $binDir to your user PATH"
    $newPath = "$binDir;$userPath"
    [Environment]::SetEnvironmentVariable("Path", $newPath, "User")
}

# Refresh PATH in the current session so `fa` works immediately.
$env:Path = "$binDir;" + [Environment]::GetEnvironmentVariable("Path", "Machine") + ";" + [Environment]::GetEnvironmentVariable("Path", "User")

$faCmd = Get-Command fa -ErrorAction SilentlyContinue
if (-not $faCmd) { $faCmd = Get-Command fah -ErrorAction SilentlyContinue }
if ($faCmd) {
    Write-Ok "'fa' is on PATH ($($faCmd.Source))."
} else {
