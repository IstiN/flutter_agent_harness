function Read-Choice($prompt, $valid) {
    while ($true) {
        $value = Read-Host $prompt
        if ($valid -contains $value) { return $value }
        Write-Warn "enter one of: $($valid -join ', ')"
    }
}

function Read-Secure($prompt) {
    $secure = Read-Host $prompt -AsSecureString
    $cred = New-Object System.Net.NetworkCredential("", $secure)
    return $cred.Password
}

